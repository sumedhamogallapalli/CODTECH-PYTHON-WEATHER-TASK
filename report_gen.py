# 3. ANALYSIS & AUTOMATION ENGINE
# ==========================================
def generate_automated_report():
    # Load and Analyze Data
    if not os.path.exists("sales_data.csv"):
        create_sample_csv()

    df = pd.read_csv("sales_data.csv")
    total_revenue = df['Revenue'].sum()
    avg_sales = df['Units_Sold'].mean()
    top_product = df.loc[df['Revenue'].idxmax(), 'Product']

    # --- Step A: Create Visualization ---
    plt.figure(figsize=(10, 6))
    plt.bar(df['Product'], df['Revenue'], color='#3498db', edgecolor='#2980b9')
    plt.title('Total Revenue by Product Category', fontsize=14)
    plt.ylabel('Revenue in USD ($)')
    plt.grid(axis='y', linestyle='--', alpha=0.7)
    plt.savefig("sales_chart.png", dpi=300)
    plt.close()

    # --- Step B: Build PDF ---
    pdf = PDFReport()
    pdf.add_page()

    # Section 1: Business Summary
    # (Note: Bullet point replaced with '-' to avoid UnicodeEncodeError)
    pdf.chapter_title("1. Business Executive Summary")
    pdf.set_font('Arial', '', 12)
    pdf.cell(0, 10, f"- Total Revenue Generated: ${total_revenue:,}", 0, 1)
    pdf.cell(0, 10, f"- Average Units Sold per Category: {avg_sales:.2f}", 0, 1)
    pdf.cell(0, 10, f"- Top Performing Product: {top_product}", 0, 1)
    pdf.ln(10)

    # Section 2: Data Table
    pdf.chapter_title("2. Detailed Inventory Sales Data")
    pdf.create_table(df)
    pdf.ln(15)

    # --- SMART LAYOUT LOGIC ---
    # We check the Y position. If we are too low on the page,
    # we move to a new page to keep the heading and chart together.
    if pdf.get_y() > 150:
        print("Adding page break to keep Chart and Heading together...")
        pdf.add_page()

    # Section 3: Visualization
    pdf.chapter_title("3. Revenue Distribution Chart")
    pdf.image("sales_chart.png", x=10, w=190)

    # Output the File
    try:
        pdf.output("Sales_Analysis_Report_Final.pdf")
        print("\n" + "="*50)
        print("SUCCESS: Professional PDF Report generated!")
        print("Filename: Sales_Analysis_Report_Final.pdf")
        print("="*50)
    except Exception as e:
        print(f"Error saving PDF: {e}")

if __name__ == "__main__":
    generate_automated_report()