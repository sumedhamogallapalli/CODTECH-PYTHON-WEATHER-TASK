import pandas as pd
import matplotlib.pyplot as plt
from fpdf import FPDF
import datetime
import os

# ==========================================
# 1. DATA PREPARATION
# ==========================================
def create_sample_csv():
    """Generates a sample sales dataset for the report."""
    data = {
        'Product': ['Laptops', 'Mobiles', 'Tablets', 'Monitors', 'Keyboards', 'Mice'],
        'Units_Sold': [50, 120, 80, 60, 200, 150],
        'Unit_Price': [1000, 600, 400, 300, 50, 30],
        'Revenue': [50000, 72000, 32000, 18000, 10000, 4500]
    }
    df = pd.DataFrame(data)
    df.to_csv("sales_data.csv", index=False)
    print("Step 1: Data file 'sales_data.csv' prepared.")

# ==========================================
# 2. PDF REPORT GENERATOR CLASS
# ==========================================
class PDFReport(FPDF):
    def header(self):
        """Creates a consistent header for every page."""
        self.set_font('Arial', 'B', 20)
        self.set_text_color(44, 62, 80) # Corporate Dark Blue
        self.cell(0, 15, 'MONTHLY SALES PERFORMANCE REPORT', 0, 1, 'C')
        self.set_font('Arial', 'I', 10)
        self.set_text_color(100, 100, 100)
        self.cell(0, 5, f'Generated on: {datetime.datetime.now().strftime("%Y-%m-%d %H:%M")}', 0, 1, 'C')
        self.ln(10)

    def footer(self):
        """Adds page numbers at the bottom of every page."""
        self.set_y(-15)
        self.set_font('Arial', 'I', 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, f'Page {self.page_no()}', 0, 0, 'C')

    def chapter_title(self, title):
        """Formats section headings with a background color."""
        self.set_font('Arial', 'B', 14)
        self.set_fill_color(200, 220, 255) # Light Blue Background
        self.set_text_color(0, 0, 0)
        self.cell(0, 10, title, 0, 1, 'L', 1)
        self.ln(5)

    def create_table(self, df):
        """Generates a formatted table from a DataFrame."""
        self.set_font('Arial', 'B', 12)
        self.set_fill_color(44, 62, 80)
        self.set_text_color(255, 255, 255)

        w = 47
        for col in df.columns:
            self.cell(w, 10, col, 1, 0, 'C', 1)
        self.ln()

        self.set_font('Arial', '', 12)
        self.set_text_color(0, 0, 0)
        for _, row in df.iterrows():
            self.cell(w, 10, str(row['Product']), 1)
            self.cell(w, 10, str(row['Units_Sold']), 1, 0, 'C')
            self.cell(w, 10, f"${row['Unit_Price']:,}", 1, 0, 'R')
            self.cell(w, 10, f"${row['Revenue']:,}", 1, 0, 'R')
            self.ln()

